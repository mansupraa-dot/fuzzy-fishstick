export function getProductById(id) {
  return PRODUCTS.find((p) => p.id === Number(id))
}

export function getPopularProducts(count = 4) {
  return PRODUCTS.filter((p) => p.isPopular).slice(0, count)
}

export function getProductsByCategory(category, subcategory = null) {
  return PRODUCTS.filter(
    (p) => p.category === category && (!subcategory || p.subcategory === subcategory)
  )
}
